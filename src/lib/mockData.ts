// Fonction pour simuler l'API avec filtres
export function filterMockProfessionals(params: {
  profession?: string
  city?: string
  search?: string
  page?: number
  limit?: number
}) {
  let filtered = [...mockProfessionals]

  // Filtrer par profession
  if (params.profession) {
    filtered = filtered.filter(p => p.profession === params.profession)
  }

  // Filtrer par ville
  if (params.city) {
    filtered = filtered.filter(p => 
      p.city.toLowerCase().includes(params.city!.toLowerCase())
    )
  }

  // Recherche textuelle
  if (params.search) {
    const searchTerm = params.search.toLowerCase()
    filtered = filtered.filter(p => 
      p.first_name.toLowerCase().includes(searchTerm) ||
      p.last_name.toLowerCase().includes(searchTerm) ||
      p.bio?.toLowerCase().includes(searchTerm) ||
      p.profession.toLowerCase().includes(searchTerm)
    )
  }

  // Pagination
  const page = params.page || 1
  const limit = params.limit || 10
  const total = filtered.length
  const totalPages = Math.ceil(total / limit)
  const start = (page - 1) * limit
  const end = start + limit
  const paginatedResults = filtered.slice(start, end)

  return {
    professionals: paginatedResults,
    pagination: {
      page,
      limit,
      total,
      totalPages,
      hasMore: page < totalPages
    }
  }
}

